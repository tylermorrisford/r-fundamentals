# VARIABLES in R

# pound signs begin comments! Execute a line, put cursor on line and hit 'Run'

# INTEGER - assigning 2L (integer) to x
x <- 2L
typeof(x)

# DOUBLE (anything with a decimal point)

y <- 2.5
typeof(y)

# COMPLEX
z <- 3 + 2i
typeof(z)

# CHARACTER
a <- "a"
typeof(a)


# LOGICAL
true <- T # true can be either T or TRUE, must be in all caps
typeof(true)


false <- FALSE # again, all caps
typeof(false)

